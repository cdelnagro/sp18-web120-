<h1 class="pageID">Client Survey Considerations</h1>
<section>
<p><h2>Why do you want a new website?</h2>
<p>Before you can understand your client's customers motivations, you need to 
understand the client's motivations.</p>
<h2> Who is your website trying to reach?</h2> 
<p>Open your ears and mind when your client is talking about who the 
    new website is for. Take notes, identify the types of people who are 
    currently or may be using the site, just don't feel obligated to make 
    new people up along the way. </p>
<h2> What do you want these people to do when they get to your website? </h2> 
<p>Yes, this is about defining calls to action.  What the client wants 
    from their website and what its users want are two different things. 
    Your job is to find that sweet spot in the middle. 
<h2> How are you going to promote the new site? </h2>
<p>There are several reasons to ask this question. But the main one is 
    that it will help you to gauge how serious the client is about the 
    process they are about to undertake.</p> 
<h2> How will you know if the new site has been successful? </h2>
<p>This is another question which can elicit blank stares from a client. 
    If they are not already familiar with the use of analytics platforms, now is your chance to educate them. 
<h2>When do you want to launch the new site?</h2>
<p>Does the client have any specific deadlines? Or maybe an event they 
    want the launch to coincide with? Finding out sooner rather than 
    later will make your job a lot less stressful. 
<h2> Do you have a domain name (and hosting) in place yet?</h2>
<p>It is important to get the domain name and hosting requirements 
    out in the open as early as possible.</p>
<h2>Who is going to maintain the new siteâ€™s content? Do you have a 
    content plan?</h2> 
<p>Whoever the Web Dev is going to be, make sure you have all salient 
    points covered under a contract of some kind, so you and your new 
    client know going forward what steps will need to be taken  to 
    smaintain the sites relevance going forward.</p>
