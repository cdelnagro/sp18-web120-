<h1>Responsive Website or Native App?</h1> 
    
<p>This question is posed by many when embarking on a path to marketing your business and is a lot like who came first, the chicken or the egg?  Developing a responsive website or a native app takes thought and consideration regarding several factors prior to development. There are advantages to both as well as differences between the two. I will focus on  the differences and advantage/disadvantages of each.</p>
    
<p>A responsive website is optimized and scaled for mobile devices.  They are compatible with any operating system and screen resolution. The site should make sure your pages look good over all platforms be it desktop, tablet or mobile phone. I personally will go to a website first to view the contents of that site and then decide if I want to download that app. Now that Google is prioritizing responsive sites in the search results algorithm, itâ€™s essential to make sure your site is optimized for mobile by using responsive design.</p>        

<p>A Mobile app is a full-featured application. It is developed for dedicated platforms which can operate offline as well as online. The app must be downloaded from a respective online marketplace and installed on the phone. The advantage to the mobile app is that the function provides immediate interaction for the user.  A big advantage to the mobile app over the responsive app is the apps ability to send push notifications to the user on occasion to enhance the experience of that app.</p>

<h2>Your considerations prior to development are:</h2>
<ul>
    <li>	Cost of development. It is much cheaper to develop and maintain a website than a mobile app.</li>
    <li>	Using SEO can get the word out there much faster about your website than can your app because the app is a closed environment and cannott be searched using a search engine.</li>
    <li>	The skill level to create a responsive website is less than creating a native app. To build native apps, you need a special skill set and resources.</li> 
    <li>	An advantage to a native app is that it has heightened security. Developers can use encryption technology to ensure critical information is not stored at the application level. With a website, we are often subjected to phishing scams and hacks.</li>
</ul>
    
<p>In the current marketplace there are advantages and disadvantages to both. Your mobile website will act as your business calling card allowing the user to experience your offering with the hope that they will become a customer.  The native app provides the most optimal customer experience. Native applications are sexy and the wave of the future. They allow you to create a personalized marketing campaign and device specific functions such as maps, camera and calendar functions.</p>
    
<p>In general, it is recommended that your company have a great web presence first, then down the road, develop that native app for the personal engagement with your customer.</p>
    
