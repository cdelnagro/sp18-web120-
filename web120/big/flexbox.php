<h1>Flexbox Research</h1>
    
<h3>What is Flexbox?</h3>
The Flexible Box Layout Module is the newest layout mode of CSS3 
properties that work together to lay the content of your website 
responsively. Described by W3C, Flexbox has been designed for 
laying out UI elements such as a menu but is not intended for 
use on a whole page. It has been created to improve items 
alignment, directions and order in a container when they
are dynamic or unknown. It solves many layout problems. The 
Flexbox layout algorithm is based on direction unlike 
block or inline layout which is based on a horizontal/vertical 
axis. Browser support for Flexbox is comprehensive. 

<h3>Basic Concepts</h3>
        
Flexbox uses two types of boxes not used before; flex containers and flex items.  
The container is designed to group items together and define how they are positioned. 
The main purpose of the flex items is to let their container know how many things it 
needs to position. You will align the flex items inside 
the container and those items can also serve as a flex container for their own items. 
When you are handed a mockup to implement, your first draw a bunch of boxes on 
that piece of paper to determine how you are supposed to stack, stretch and shrink 
to your desired design. Once you have taken this stop it will be easier to code using 
the new flexbox techniques. 

<h3>Benefits</h3>

    Two major benefits to using flexbox: You will be able to do more with layouts, 
and you will write less code to create them. 
A few other benefits are:	
1.	Flexbox is easy to get started. 
2.	Flexbox offers painless vertical and horizontal centering of items.
3.	With Flexbox, Inline elements are easily manipulated. 
One of the greatest things about flexbox is the flexibility of the concept. Add 
a little here, a little there as you like. Flexbox can be used for one layout 
component, a few, or to build an entire site using flexbox.	
    
