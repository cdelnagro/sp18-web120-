        <h1>Photo Galleries for Websites</h1>

        <p> A visually appealing website starts with a solid design and a conscious color scheme. A well-designed sight communicates competency, care and credibility. A poorly designed site can trigger apprehension and can shut down interest within a few seconds.</p>

        <p> Choosing a photo gallery plugin is a major web design decision. Your choice of a software plugin can determine how photos are framed, aligned, sized and organized. When choosing, what you could look for an interface that is full of features. A few suggestions on what to look for are:</p>

        <ul>
            <li> Look for software that is responsive and that is supported across all browser platforms.</li>
            <li> A thumbnail grid is important, so search for a site with a thumbnail preview. A thumbnail preview is a way to display images for easy viewing.</li>
            <li> Search for a feature that uses a lightbox. A lightbox feature is one that puts a frame around your picture and darkens everything else thereby enhancing your photo. For example, when you click on a photo in Facebook, it pops up bigger with a dark frame. This reduces eye strain and helps to keep the user focused on that photo gallery plugin. </li>
        </ul>

        <h2>Photo gallery software companies to consider:</h2>
        <ul>
            <li> Lightbox by Lokesh Dahkar  http://lokeshdhakar.com/projects/lightbox2/. Lightbox has clearly written instructions on this site and is easy to use. It is also free.</li>

            <li> Juicebox  https://www.juicebox.net/. Juicebox is responsive, has an easy to use gallery creation tool. They also offer SEO, SMO and shopping cart support. Free Lite is a version that you do not have to pay for.</li>

            <li> Dynamic Drive CSS Gallery - http://www.dynamicdrive.com/style/csslibrary/item/css-image gallery/. Dynamic Gallery offers a CSS based image gallery that displays larger versions of thumbnail images that enlarge when the mouse hovers over them.</li>
        </ul>
