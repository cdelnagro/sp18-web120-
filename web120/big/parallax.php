<h1>Parallax</h1>
<p>Imagine yourself sitting in a vehicle driving down the road. You become drowsy, you are dreaming. As you pass the objects on the road, you see some that are closer to you and you view them as moving by swiftly. As the vehicle continues, other images in the back ground appear to move by more slowly.  Your mind slows, you are in a mindful state. That awareness of the effect is called the parallax effect which replicates the experience of the foreground objects moving faster than the background objects.</p>
    
<p>This effect is seen as part of a scrolling feature of a web page. It uses multiple background images which seem to move at different speed to create a sensation of depth. It creates an interesting browsing experience. It is fluid and visually appealing. The term parallax comes from the Greek word parallax  meaning alteration.  It is very cool!  </p>
    
